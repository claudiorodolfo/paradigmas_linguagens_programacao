//Rust
fn main() {
    let inteiro_pequeno: i8 = 100;               // Inteiro pequeno
    let inteiro: i32 = 1000;                     // Inteiro
    let inteiro_grande: i64 = 1000000;           // Inteiro grande
    let ponto_flutuante_simples: f32 = 3.14;     // Ponto flutuante simples
    let ponto_flutuante_duplo: f64 = 3.14159265359; // Ponto flutuante de dupla precisão
    let booleano: bool = true;                   // Booleano
    let caractere: char = 'A';                   // Caractere
    let inteiro_sem_sinal: u32 = 2000;           // Inteiro sem sinal

    println!("Inteiro pequeno: {}", inteiro_pequeno);
    println!("Inteiro: {}", inteiro);
    println!("Inteiro grande: {}", inteiro_grande);
    println!("Ponto flutuante simples: {}", ponto_flutuante_simples);
    println!("Ponto flutuante de dupla precisão: {}", ponto_flutuante_duplo);
    println!("Booleano: {}", booleano);
    println!("Caractere: {}", caractere);
    println!("Inteiro sem sinal: {}", inteiro_sem_sinal);
}
