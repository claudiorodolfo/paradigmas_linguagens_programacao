#Python
str1 = "Olá"
str2 = "Mundo"

# Concatenar
concatenado = str1 + " " + str2
print(f"Concatenado: {concatenado}")

# Substring
sub = concatenado[:3]
print(f"Substring: {sub}")

# Comparar strings
iguais = str1 == str2
print(f"As strings são iguais? {iguais}")

# Tamanho da string
print(f"Tamanho de str1: {len(str1)}")
